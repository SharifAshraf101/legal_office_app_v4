// Generates "professional" theme background overrides from the existing dark
// theme rules. The professional theme runs WITH the `dark` class (so dark's
// component overrides apply), but dark hardcodes slate hex backgrounds across
// many high-specificity rules that variable overrides can't reach. For every
// `body.dark …{ …background:#<dark hex>… }` rule we emit a twin
// `body.professional.dark …{ background:#<deep blue> }` — one class more
// specific, so it always wins — turning every dark surface deep blue.
//
// Idempotent: rewrites the marked block between BEGIN/END markers in globals.css.
import { readFileSync, writeFileSync } from 'node:fs';

const CSS = 'app/globals.css';
const BEGIN = '/* === BEGIN generated professional-theme overrides === */';
const END = '/* === END generated professional-theme overrides === */';

// Neutral (slate/grey/white) TEXT colors → silver. Accent text colors
// (greens, reds, ambers, blues, purples, teals) are intentionally absent so
// they're left untouched — only the hard-to-read neutral text turns silver.
const TEXT_MAP = {
  '#ffffff': '#c0c0c0',
  '#f8fafc': '#c0c0c0',
  '#f1f5f9': '#c0c0c0',
  '#f9fafb': '#c0c0c0',
  // The dark theme's own --text / --muted values, hardcoded across ~1600 rules.
  '#dbe4f0': '#cfd4db',
  '#e6edf7': '#cfd4db',
  '#e5eefb': '#cfd4db',
  '#aebbd0': '#c0c0c0',
  '#e2e8f0': '#c6c6c6',
  '#e5e7eb': '#c6c6c6',
  '#d1d5db': '#c0c0c0',
  '#cbd5e1': '#c0c0c0',
  '#9ca3af': '#b4bcc8',
  '#94a3b8': '#b4bcc8',
  '#64748b': '#aeb6c2',
  '#475569': '#aeb6c2',
  '#334155': '#aeb6c2',
};

// Dark slate hex → deep-blue equivalent (by elevation level).
const MAP = {
  '#020617': '#06152c',
  '#0b1220': '#0a2342',
  '#0b1530': '#0a2342',
  '#0a1322': '#0a2342',
  '#0f172a': '#06152c',
  '#111827': '#103257',
  '#15233b': '#0e2d50',
  '#172033': '#103257',
  '#1f2937': '#143a63',
  '#1f2a3d': '#143a63',
  '#243247': '#143a63',
  '#1e293b': '#143a63',
  '#263449': '#1b4576',
  '#334155': '#1b4576',
  '#475569': '#1e4a7a',
  '#1e3a8a': '#15315e',
};

const remapHexes = (value) =>
  value.replace(/#[0-9a-fA-F]{6}/g, (h) => MAP[h.toLowerCase()] || h);

let css = readFileSync(CSS, 'utf8');

// Strip any previously generated block so re-runs stay clean.
const startIdx = css.indexOf(BEGIN);
if (startIdx !== -1) {
  const endIdx = css.indexOf(END);
  css = css.slice(0, startIdx).replace(/\s*$/, '\n') + css.slice(endIdx + END.length).replace(/^\s*/, '\n');
}

// Parse from a COMMENT-STRIPPED copy so comment text (which can contain braces,
// e.g. specificity notes) never leaks into / breaks generated selectors. The
// original `css` (with comments) is what we write back.
const parseSrc = css.replace(/\/\*[\s\S]*?\*\//g, '');

const remapTextHexes = (value) =>
  value.replace(/#[0-9a-fA-F]{6}/g, (h) => TEXT_MAP[h.toLowerCase()] || h);

// Match flat rule blocks (innermost — works inside @media too) that target dark.
const ruleRe = /([^{}]*\bbody\.dark\b[^{}]*)\{([^{}]*)\}/g;
// `(^|[^-])color` avoids matching `background-color` as a text color.
const declRe = /(?:^|[\s;])(background(?:-color)?|color)\s*:\s*([^;]+)/gi;

const out = [];
let m;
while ((m = ruleRe.exec(parseSrc)) !== null) {
  const selector = m[1].trim();
  const body = m[2];
  // Collect declarations that hardcode a mapped hex (NOT var()-based — those
  // already resolve to the professional token). Backgrounds → deep blue;
  // neutral text colors → silver (accent text is left untouched).
  const decls = [];
  let d;
  declRe.lastIndex = 0;
  while ((d = declRe.exec(body)) !== null) {
    const prop = d[1].toLowerCase();
    const value = d[2].trim();
    if (value.includes('var(')) continue;
    const hexes = value.toLowerCase().match(/#[0-9a-f]{6}/g) || [];
    const isText = prop === 'color';
    const map = isText ? TEXT_MAP : MAP;
    if (!hexes.some((h) => map[h])) continue;
    const important = /!important/i.test(value);
    const cleaned = value.replace(/\s*!important/i, '');
    const remapped = isText ? remapTextHexes(cleaned) : remapHexes(cleaned);
    decls.push(`${prop}: ${remapped} !important`);
  }
  if (!decls.length) continue;
  // Bump specificity + scope to professional: body.dark → body.professional.dark
  const proSelector = selector.replace(/\bbody\.dark\b/g, 'body.professional.dark');
  out.push(`${proSelector}{${decls.join(';')}}`);
}

const block = `${BEGIN}\n/* Auto-generated by scripts/gen-professional-theme.mjs — do not edit by hand. */\n${out.join('\n')}\n${END}\n`;

writeFileSync(CSS, css.replace(/\s*$/, '\n') + '\n' + block, 'utf8');
console.log(`Generated ${out.length} professional background overrides.`);
